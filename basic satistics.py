import numpy as np
data = np.array([1, 2, 3, 4, 5])

# Mean
mean_value = np.mean(data)
print("Mean:", mean_value)

# Median
median_value = np.median(data)
print("Median:", median_value)

# Standard deviation
std_dev = np.std(data)
print("Standard Deviation:", std_dev)

# Sum
sum_value = np.sum(data)
print("Sum:", sum_value)
